class Habitacion():
    def __init__(self, nombre, habitacion_izquierda, habitacion_derecha, objetos_dentro, plano ):
        self.nombre = nombre
        self.habitacion_izquierda = habitacion_izquierda
        self.habitacion_derecha = habitacion_derecha
        self.objetos_dentro = objetos_dentro
        self.plano = plano

    def nombre_habitacio(self): # Funcion para imprimir el nombre
        print(self.nombre)
    
    def habitacion_de_izquierda(self): # Funcion para imprimir el nombre de la habitacion izq
        print(self.habitacion_izquierda)
    
    def habitacion_de_derecha(self): # Funcion para imprimir el nombre de la habitacion der
        print(self.habitacion_derecha)
    
    def lista_objetos(self): # Funcion para imprimir los objetos
        print(self.objetos_dentro)
    
    def plano_habitacion(self): # Funcion para imprimir el plano
        print(self.plano)
        for index, objeto in enumerate(self.objetos_dentro):
            print('        ' + str(index + 1) + '. Inspeccionar ' + objeto['name'])

